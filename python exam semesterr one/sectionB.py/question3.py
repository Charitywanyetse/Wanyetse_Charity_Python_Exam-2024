def grade_students():
        
        marks = int(input("Enter the marks of the student: "))
        
        if marks>=90:
            print("Grade A")
            
        elif marks>=89 and marks<=80:
            print("Grade B")
            
        elif marks>=79 and marks<=70:
            print("Grade C")
            
        elif marks>=69 and marks<=60:
            print("Grade D")
            
        else:
            print("Grade F") 
               
grade_students()            
            
            
